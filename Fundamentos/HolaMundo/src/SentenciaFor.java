public class SentenciaFor {
    public static void main(String[] args) {

        // Sentencia for para incrementación.
        for ( int i = 0; i < 10 ; i++){
            System.out.println("i = " + i);
        }

        // Sentencia for para decrementación.
        for ( int i = 10; i >= 0 ; i--){
            System.out.println("i = " + i);
        }
    }
}
